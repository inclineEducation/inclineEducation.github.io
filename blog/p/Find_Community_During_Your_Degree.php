        I chose UBC Vancouver for the engineering options it would offer me. 
        Coming from the Okanagan, where the UBCO campus is located, the decision to move to 
        Vancouver was uncommon. It was so uncommon that I arrived on the UBC campus 
        without a single person I knew.
        </p> 
        
        <p>
        Of course, UBC had structures to help students get to know each other.  
        In my first week in residence, I made friends with a large portion of my 
        orientation group, and I spent the most of my first year with these people. 
        However, the bonds we made were primarily focused on all being new to UBC engineering. 
        The group deteriorated over the summer of first year, and I lost contact with most of 
        them as I entered my specialization. Just as I had been the only person 
        I knew to come to Vancouver, I was the only one to be admitted to 
        Manufacturing engineering, and was again faced with forming relationships based on proximity. 
        </p>
        <p>
        While I admit I had gotten used to the routine of making friends, drifting away, 
        and making new friends, I knew that it wasn’t sustainable for my own well-being. 
        I wanted a sense of community that wouldn’t fall apart as soon as my situation changed.
         The community in Manufacturing is great, and I’m glad to be in such a tight-knit group. 
         However, I’m extending my degree, and therefore facing a year in university where 
         most of them will have graduated.

        </p> 

        <p>
        During my first year, I made a friend that joined the engineering sorority. 
        My hesitation to join a sorority was the reason I didn’t join in my first year, 
        but the missing sense of community was the reason that I chose to take the plunge near 
        the start of my second year. I was accepted into the fold, and now my whole 
        experience of UBC engineering has changed. I have friends in various years 
        and specializations, giving me access to advice and perspective that I 
        wouldn’t have otherwise gotten.  I learned secrets to the best courses 
        to take, or profs to watch out for, and didn’t feel like an outlier when I did 
        poorly on a midterm.  People that have already been through my situation 
        give the best advice on how to work out of it.
        </p>

        <p>
          It was in my grade 11 and 12 years that it finally 'clicked' for me, instead of spreading myself thin over activities that I did not love, I needed to double
          down on what I enjoyed doing from the beginning -- making videos. I looked back over the then five years of videos I had made and realized that this was the
          'EC' I was looking for: something I cared about and naturally did.
        </p>

        <p>
        Additionally, I’ve been slowly pulled into the UBC engineering student body, the EUS. 
        I attended exactly one EUS event in first year, mainly due to the fear of the 
        “scary upper years”.  After getting to know some of them, and realizing that they aren’t 
        scary, I started going to events with friends.  I like free food as much as the next 
        undergrad, and it was a needed break from the stress of assignments and exams. 
        Now I hold a volunteer position within the EUS, and plan to be part of the organization 
        until I graduate.
        </p> 
        
        <p>
        Through my sorority and the EUS, I’ve developed better on-campus study habits, made 
        friends with people I never would’ve met otherwise, and generally felt more stable in 
        my university experience. I now have a group of people that I can rely on regardless of 
        our physical distance, which is especially important given the situation that we found 
        ourselves in at the end of the last semester. 
        </p>

        <p>
        While going to classes and handing in your work is important, there’s an underestimated 
        value in finding the people you click with. While the path I took isn’t the right way 
        for everyone, I encourage you to make friends outside of your current situational circle. 
        After all, we’re only going to be working towards a degree for a short time, 
        and I don’t plan on entering the workforce with the same number of friends that I entered university with.
        </p>
        

