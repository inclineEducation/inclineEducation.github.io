        <h3>Writer's Block</h3>
        <h2>Getting out of bed</h2>  
        <p>
        I lie on my bed and ponder how to start this blog but nothing is coming to me. 
        It’s pathetic that I’ve hit writer’s block on the first sentence. Checking my phone I see 
        that it’s nearing one o’clock. I have an assignment due this weekend that I have yet to begin... 
        I struggle to find the motivation to do it. 
        </p> 
        
        <p>
        I don’t feel like seeing friends or going out today. Yet  I  don’t feel like staying in bed 
        all day either. <b>It feels like there’s nothing I want to do today but I also don’t feel 
        like doing nothing.</b>
        </p>

        <p>
        This <b>paradoxical feeling</b> isn’t new to me, I’ve been suffering from poor mental health for a while 
        now. Although today seems to be bad, it is incomparable to some of my worst days. I look up to 
        my roof for inspiration for writing this post. I’m greeted with a poster of Arnold 
        Schwarzenegger.  
        </p>

        <img src="../i/7/arnie_stamp.jpg" align='right' style='max-width: 30vw; max-height: 70vh; min-width: 320px; height: auto; padding-left: 1vw; padding-right: 1vw;'/>
        
        <h2>The Terminator's struggle</h2>
        <p>
        Many people know the iconic figure as an actor, others may know him as the former governor 
        of California. To me, he is an inspiration. Not many people know how <b>difficult his life was 
        early on</b>, people understandably only know him after he reached fame.
        </p>

        <p>
        Schwarzenegger did have a very successful career yet even he suffered from mental health 
        issues. When he became Mr. Olympia, <b>Schwarzenegger suffered from various body image issues.</b> 
        It’s hard to fathom that the man with such a sought-after body can suffer from such issues.
        </p>

        <p>
        He openly admitted that the <b>stress placed on him due to studying, filming, and bodybuilding 
        took a massive toll on his mental health</b>. These issues were neglected, and later severely 
        <b>affected his ability to hit the books, hit the gym, and film.</b> In fact, his mental health 
        deteriorated so much that he admitted that he used to throw up when he saw himself in the mirror.
        </p>

        <h2>Lessons from Mr. Olympia</h2>
        <p>
        If there’s something to learn from Schwarzenneger, it’s that <b>maintaining good grades and 
        being a good student is important</b>, but so is your health. I say "health" instead of just 
        "mental health" because <b>physical health, mental health, and social health go 
        hand-in-hand.</b>
        </p>

        <h3>A Dangerous Cycle</h3>
        <h2>Donny Boy</h2>
        <p>
        One of my favourite fitness coaches, Don Saladino (lovingly called “Donny Boy” by my friends 
        and I) who is known for training Sebastian Stan and Ryan Reynolds, described <b>everyday life 
        as a series of different stresses and that our health is our capacity to handle those stresses.</b> 
        </p>

        <p>
        Donny Boy preaches the <b>importance of recovery and not to stress your body more than 
        it can take. Recovery is vital for the body’s ability to absorb the stresses it has endured and 
        get stronger.</b> This idea is important to bodybuilding: as muscles get 
        stressed and they have a period of recovery, they come back bigger and stronger.
        </p>

        <p>
        I always thought this only applied to bodybuilding but I realize now that this is also <b>applicable 
        to our overall health.</b>
        </p>

        <h2>The self-descrutive cycle</h2>
        <p>
        As you can see from my poorly drawn clipart below, <b>too much stress causes your health 
        (mental and physical) to decline, which can add more stress to your body 
        causing your health decline further and so on.</b> It’s quite a vicious cycle.
        </p>
        <img src="../i/7/cycle.png" align='left' style='max-width: 30vw; max-height: 70vh; min-width: 320px; height: auto; padding-left: 1vw; padding-right: 1vw;'/>
        <p>
        So how do we take care of our health during stressful times? It is easy to preach but hard 
        to practice. Let’s break health down into three main components: <b>physical, mental, and 
        social. To take care of your health you must focus on taking care of these three elements.</b>
        </p>

        <h3>Physical Health</h3>
        <h2>Eating</h2>
        <p>
        Physical health is simple enough to take care of. It’s vital that you are <b>eating enough</b> 
        throughout the day and getting some physical activity. No matter how much work you may 
        have, <b>you cannot sacrifice eating.</b> Many people recommend eating 3 meals a day, but 
        personally eating 2 big meals has worked out fine for me - I find this schedule to be 
       more time-efficient, but finding an eating pattern that works best for you is imperative.
        </p>

        <p>
        The average male needs to eat around 2500 calories, whereas, the average female needs around 
        2000 calories. However, you should ensure <b>you’re 
        not just binging on snacks, as you need the right amount of carbs, fats (don’t be scared of 
        them), and protein.</b> A meal plan helps properly schedule and document your eating habits. 
        </p>

        <p>
        If you run into trouble while making a meal plan, remember there are lots of online resources 
        to assist you!  This website leads you to a great meal plan generator: 
        <a href = 'https://www.eatthismuch.com/'>Eat This Much: The Automatic Meal Planner</a>. Obviously, you <b>don’t 
        need to follow it religiously</b> and you can definitely make some wiggle room for the 
        foods you like.
        </p>

        <p>
        I am quite into bodybuilding so I have a pretty strict meal plan, but I also have an 
        insatiable sweet tooth. So instead of outright going “cold turkey” on sweets, I incorporate 
        small amounts of it into my diet. This makes me feel good and helps me stick to my meals. 
        Make sure your meal plan is enjoyable.
        </p>

        <h2>Exercise</h2>
        <p>
        Obviously you should make sure you’re getting enough exercise. Exercise doesn’t need to be 
        a whole CrossFit routine in the gym, although if that’s what you like go for it. <b>Exercise 
        can be as simple as a 30-minute jog</b> throughout your neighbourhood a couple of days a week. 
        You’ll come back <b>feeling refreshed and ready to tackle</b> whatever you have to do. Make sure 
        if you are <b>exercising that you eat properly.</b>
        </p>
        <img src="../i/7/flex1-copy.jpg" align='right' style='max-width: 30vw; max-height: 70vh; min-width: 320px; height: auto; padding-left: 1vw; padding-right: 1vw;'/>

        <h2>Sleep</h2>
        <p>
        <b>Proper rest is important as well.</b> Although 7-9 hours of sleep is 
        recommended, I know it can be hard getting that amount so <b>at least aim 
        for 6 hours.</b> Your <b>circadian rhythm resets every 1hr and 30 mins. Most scientists 
        recommend that your circadian rhythm should repeat 5 times (7hr and 30 mins)</b> for a good 
        night’s sleep. However, <b>other people say that only repeating 4 times (6 hrs) is good enough.</b>
        </p>

        <h3>Mental Health</h3>
        <h2>Personal Time</h2>
        <p>
        Next is mental health. This one isn’t as easy as physical health. Improving your physical 
        health is pretty similar for everybody but improving mental health is unique to every 
        person. The <b>key thing is giving yourself some “you time”.</b> Find something 
        you really enjoy and dedicate at least thirty minutes a day to doing it.
        </p>
        
        <p>
        I enjoy the guitar so I dedicate quite a bit of time playing or writing songs. The idea is 
        to make sure you have a <b>bit of time every day to alleviate your stresses</b> and allow 
        your brain to rest. <b>Exercising and eating well has a positive effect on your mental 
        health</b> (as can the occasional sweet treat). Not to mention, <b>sleeping enough is essential to 
        good brain health.</b>
        </p>

        <p>
        You can see why I grouped physical and mental health together; there is <b>a lot of overlap 
        between the two.</b> Improving one tends to improve 
        the other.
        </p>

        <h3>Social Health</h3>
        <h2>Don't be a lone wolf</h2>
        <p>
        <b>Social health is incredibly important. Having a good supportive group of friends and adults is essential. </b>
        </p>

        <p>
        <b>We need people.</b> There are a lot of things we can do 
        ourselves but there’s an equal (if not, greater) number of things we can’t do ourselves.
        </p>

        <h2>Be mindful of who you surround yourself with</h2>
        <p>
        The <b>people we surround ourselves with have a great impact on who we are and who we become.</b> 
        A common proverb says that you can <b>tell a lot about a person based on their closest group 
        of friends.</b> Make sure 
        <b>you surround yourself with people who are good for you, and whom you feel comfortable opening up to.</b> 
        </p>

        <p>
        Likewise, <b>ensure that you’re also there for your friends</b> if they need you. Although it may not 
        seem like it, <b>we’re all going through similar problems.</b> I think the best way to end this 
        segment would be with a quote from one of my favourite TV characters, Uncle Iroh from 
        Avatar the Last Airbender: <b><i>“while it's always best to believe in one’s self. A little 
        help from others can be a great blessing”.</i></b>
        </p>
        <img src="../i/7/roydusanta.jpeg" align='left' style='max-width: 30vw; max-height: 70vh; min-width: 320px; height: auto; padding-left: 1vw; padding-right: 1vw;'/>

        <h3>Seeking help</</h3>
        <h2>Seeking help is perfectly fine</h2>
        <p>Something I cannot emphasize enough is to <b>seek help when you need it.</b> Sometimes we 
        may let our health decline past what we think we can handle. <b>Seeking help is perfectly fine</b> and you’ll feel better as a result.
        </p>

        <p>
        If you have a physical health issue it’s important to see a doctor. However, if you're dealing with mental 
        health issues, ensure you have appropriate support, such as friends, parents, 
        adults, or a therapist. There are <b>a lot of mental health resources available</b>, like this 
        one <a href = 'https://www.canada.ca/en/public-health/topics/mental-health-wellness.html'>here</a>. 
        Likewise, <b>healthcare is mostly covered in Canada</b> so don’t hesitate to see a doctor if you need 
        one.
        </p>

        <h2>People care about you</h2>
        <p>
        When I was in a bad place I didn’t want to bother anyone with my problems 
        so I internalized them. <b>This caused a rift to open between my friends, family and me.</b> 
        I did suffer a lot because of it but when I decided to <b>open up to my parents and friends,
        they were nothing but understanding.</b> If my family and friends are reading this, I am incredibly thankful for you.
        </p>

        <p>
        <b>People who actually care about you will not judge you</b>. Instead, they’ll do 
        whatever they can to help you. When I finally <b>talked to a therapist she was very supportive 
        and helped me out a lot. Most campuses have mental health counsellors but there are a lot 
        of other resources you can use.</b> We all find ourselves struggling at some point; be mindful about it and <b>seek help when you need it.</b>
        </p>

        <h3>Perseverance is Key</h3>
        <h2>Gaining momentum</h2>
        <p>
        My final point I would like to get across is the <b>importance of perseverance</b>. Declining
        health is <b>NOT YOUR FAULT</b>. That being said, it’s your 
        <b>responsibility to overcome your problems.</b> You may 
        have days (like me) where all your motivation is gone and everything feels wrong. It may 
        feel like the world is sprinting ahead while you’re being left behind. That’s when you need 
        to <b>force yourself to start something</b>. Don't hesistate, just start it.
        </p>

        <p>
        The Law of Inertia states that an object at rest wants to stay at rest. Likewise, <b>you 
        need to give yourself that PUSH</b> to keep yourself going. Oftentimes, <b>just starting something 
        gives us the momentum we need to move along</b>. For example, doing a whole workout may seem 
        daunting and you might lack the motivation to do it, but force yourself to start the 
        warm-up. The warm-up can give the momentum you need to tackle the workout.
        </p>

        <h2>Response to problems</h2>
        <p>
        Remember that <b>no matter how bad things seem, they are temporary</b>, but <b>how you 
        respond to these problems will shape who you are in the future</b>. Perseverance is key! 
        </p>

        <p>
        I’ll end this segment with another quote that really touched me. This quote is from 
        Guy-Sensei from the show Naruto: <b><i>“Don’t let anything steer you off the path you’ve drawn 
        for yourself. Forge ahead to the end! Stick to it! Be everything you can be!”</i></b>
        </p>

        <h3>Writer's Block NO MORE</h3>
        <h2>Time stops for no one</h2>
        <p>
        Writing this blog reminded me of how bad my depression was earlier this year. Due to a 
        variety of internal and external reasons, I fell into a deep depression; a fact that shook 
        the people who knew me. I was always known as a jovial guy but <b>depression can hit 
        anyone.</b> 
        </p>

        <p>
        At the peak of my depression, my friend told me something that served as a wake-up call. 
        He told me that <b><i>“time doesn’t stop for you, it keeps moving forward at the same rate it 
        always does”.</i></b> I think he meant that everything moves on and it’ll get better but I took 
        it as the <b>world will not stop just because I feel too depressed to do anything.</b>
        </p>

        <p>
        After he told me that, <b>I tried to overcome my depression</b>. I exercised, 
        fixed my diet, slept properly, took time for myself, told my friends and family what I 
        was going through, and eventually saw a counsellor. All the advice I have given you 
        were things that I did to help myself and it worked. <b>I’m not gonna lie and say that I’ve fully 
        overcome my problems, not yet at least</b>. However, it's a good first step.
        </p>

        <h2>True victory is the sum of little victories</h2>
        <p>
        I stop staring at Arnold, smack my cheeks and force myself out of bed. I fold the 
        sheets to officially start my day. I still feel hopeless, but forcing myself to get 
        out of bed will <b>give me the momentum to get through the day</b>. I know my problems won’t 
        go away by today or even tomorrow, but <b>I can take the victory that I didn’t let these 
        problems defeat me. Instead, I persevered. These little victories add up overtime and will 
        eventually result in a monumental victory</b>. Hopefully, when I overcome my problems I will 
        come out a much stronger person.
        </p>

        <h3>Conclusion</h3>
        <h2>You are full of potential</h2>
        <p>
        Stephen King often wrote great works but had trouble sticking the landing, I hope I don’t 
        run into the same problem.
        </p>

        <p>
        I want to express that even though it seems like I have figured a lot of stuff out, the journey
        was difficult. I had good days and bad days. But as I said,
        my response to these bad days shapes who I am. If there is one thing I want you to learn from my rambling, it’s that while <b>being
        studious is admirable, it should never take priority over your health and wellbeing. Neglecting 
        your health will be detrimental in the long run,</b> so please be mindful of that. Most 
        importantly, <b>don't be afraid to ask for help and never give up!</b> 
        </p>

        <p>
        <b>You’re too full of potential to let declining health get in the way of your future</b>. 
        Good luck reader, the future is bright and you’ll do great things. Take care 
        of yourself and keep persevering! If you have any questions or would like to talk, you can email me at
        <a href="mailto: nusair.islam@inclineedu.org">nusair.islam@inclineedu.org</a>
        </p> 

        

