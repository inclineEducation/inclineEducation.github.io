        <h3>Writer’s Block NO MORE</h3>
        <h4 style = "color: #00008B">Time Stops for No One</h4>
        
        
        <p>Writing this blog reminded me of how bad my depression was earlier this year. Due to a variety of internal and external reasons I fell into a deep depression, a fact that shook the people who knew me. I was always known as a pretty happy guy but <b>depression can hit anyone.</b> </p>

      <p>  I was in a very dark place and I thought I’d just live with it and accept this is who I am now. At the peak of my depression my friend told me something that served as a wake-up call. He told me that <b>“time doesn’t stop for you, it keeps moving forward at the same rate it always does”.</b> I think he meant that everything moves on and it’ll get better but I took it as the <b>world will not stop just because I feel too depressed to do anything.</b></p> 

        <p>After he told me that,<b> I did everything I could to overcome my depression.</b> I exercised, fixed my diet, slept properly, took time for myself, told my friends and family what I was going through, and eventually seeked a counsellor. All the advice I have given you were things that I did to help myself and it worked. <b>I’m not gonna lie and say that I’ve overcome my problems, I definitely haven’t, not yet at least.</b> But now I know what I can do when I feel hopeless like I do today. </p>


    <h4 style = "color: #00008B">True Victory is the Sum of Little Victories</h4>
        
<p>        I stop staring at Arnold, I smack my cheeks, and I force myself out of bed. I fold the sheets to signify the start of my day. I still feel hopeless but forcing myself to get out of bed will <b>start the momentum I need to get through this day. </b>I know my problems won’t go away by today or even tomorrow but I <b>can take victory that I didn’t let these problems defeat me today and I persevered. These little victories add up and they will eventually result in a monumental victory.</b> Hopefully when I overcome my problems I will come out a much stronger person. </p>
        
        
        
        <h3>Conclusion</h3>
        <h4 style = "color: #00008B">You are Full of Potential </h4>
        
<p>        Stephen King often wrote great works but had trouble sticking the landing, I hope I don’t run into the same problem.
</p>
   <p>     If there is one thing I want you to take away from my rambling it’s that <b>being dedicated to your studies is admirable but not at the cost of your health and wellbeing. Neglecting your health will be detrimental in the long run</b> so please be mindful of that. Most importantly, make sure to <b>seek help when you need it. </b></p>

   <p>   <b>  Some problems may be a bit too much for you to handle on your own, in those cases seeking help is perfectly fine.</b> You’re too full of potential to let your health get in the way of your future. Good luck reader, <b>the future is bright and you’ll do great things.</b></p>

        
