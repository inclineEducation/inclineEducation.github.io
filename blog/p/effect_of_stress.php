        <h3> Writers Block </h3>
        <h4 style = "color: #00008B"> My Struggle </h4>
        <p>I lie  on my bed and ponder how to start this blog but nothing is
        coming to me. It’s pathetic that I’ve hit writer’s block on the first sentence.
        Checking my phone I see that it’s nearing one o’clock. I have an assignment due this weekend that I have yet to begin..
        I struggle to find the motivation to do it.</p>

        <p>I don’t feel like seeing friends or going out today either. Yet  I  don’t feel like staying in bed all day either.
        <b>It feels like there’s nothing I want to do today but I also don’t feel like doing nothing.</b> </p>

        <p>This paradoxical feeling isn’t new to me, I’ve been suffering from poor mental health for a while now. Although
        today seems to be bad, it is incomparable  to  some of my worst days. I look up to my roof for inspiration for writing
         this post. I’m greeted with a poster of Arnold Schwarzenegger from the 1984 Terminator movie. </p>


        <div style="margin: auto; margin-bottom: 1rem; text-align: center;">
          <img src='../i/7/arnold.png' style='max-width: 70vw; max-height: 50vh;'>
        </div>
        
        <p>This was basically how my weekday mornings went for the past year. Engineering schedules can be tough, especially when you
         are taking 6-7 courses in one term. The amount of hours you can spend studying a subject can be quite prolonged. However, the
         amount of knowledge and practical skill you can gain is astounding, and definitely worth the time and effort if you have a
         passion for engineering. In this week’s blog, I would like to share with you my (more or less) daily routine as
        an engineering student at UBC.</p>


        <h4 style = "color: #00008B"> The Terminator’s Struggle </h4>

         <p>Many people know the iconic figure as an actor, others  may know him as the former governor of California. To me, he is an
         inspiration.  Not many people know <b> how difficult his life was early on, </b> people understandably only know
          him after he reached fame </p>

          <p>Schwarzenegger did have a very successful career yet  even he <b>suffered from mental health issues.</b>
          When he became Mr. Olympia, <b>Schwarzenegger suffered from various  body image issues.</b> It’s hard to fathom
          that the man with such a sought-after body  can suffer from such issues. </p>

          <p>He openly admitted that the <b>stress placed on him due to studying, filming, and bodybuilding took a massive
          toll on his mental health.</b> These issues were neglected, and later severely  <b>affected his ability  to hit the
          books, hit the gym, and film.</b> In fact, his mental health deteriorated so much that  he admitted that he used to
           throw up when he saw himself in the mirror.</p>

        <h4 style = "color: #00008B"> Lessons from Mr. Olympia</h4>

         <p>If there’s something to learn from Schwarzenneger it’s that <b>maintaining good grades and being a good student is
         important, but so is your health.</b> I say health instead of just mental health because <b> physical health, mental health,
         and social health actually go hand-in-hand.</b></p>

          <p>From personal experience, I often let my mental health and physical health decline in order to  focus more on my studies. At one point my<b>>neglect caught up to me and
          it really started affecting my studies causing my grades to take a free fall.</b></p>

        <h3> A Dangerous Cycle </h3>
        <h4 style = "color: #00008B">Donny Boy</h4>
            
            <p>One of my favourite fitness coaches, Don Saladino (lovingly called “Donny Boy” by my friends and I) who is known for training Sebastian Stan and Ryan Reynolds, described <b>everyday life as a series of different stresses and our health is our capacity to handle those stresses.</b></p>

          <p>Donny Boy always preaches the <b></b>importance of recovery and not to stress your body more than it can take. Recovery is vital for the body’s ability to absorb the stresses it endured and get stronger so that it can handle more. </b>This idea is vital to bodybuilding, muscles get stressed and after a period of recovery they come back bigger and stronger.</p>
        
        <p>I always thought this only applied to bodybuilding but I realize now that this is <b>applicable to our health entirely.</b></p>
        
        <h4 style = "color: #00008B">The Self-Destructive Cycle</h4>
        
        
        <p>As you can see from my poorly drawn clipart below,<b> too much stress causes your health (mental and physical) to decline, this decline in health can add more stress to your body which will make your health decline further and so on </b>It’s quite a vicious cycle.</p>
        
         <div style="margin: auto; margin-bottom: 1rem; text-align: center;">
          <img src='../i/7/cycle.png' style='max-width: 70vw; max-height: 50vh;'>
        </div>
        
        
        <p>So how do we take care of our health during stressful times? It is easy to preach but hard to practice. Let’s break <b>health</b> down into three main components: <b>physical, mental, and social.</b> <b>To take care of your health you must focus on taking care of these three elements.</b></p> 
        
        <h3> Physical Health </h3>
        <h4 style = "color: #00008B">Eating</h4>
        


        <p>Physical health is simple enough to take care of. It’s vital that you are <b>eating enough throughout the day</b> and getting some physical activity in. No matter how much work you may have,<b> you cannot sacrifice eating. </b>Many people recommend eating 3 meals a day, but personally eating 2 big meals has worked out fine for me - I find this schedule to be much   more time-efficient, but finding  whatever eating pattern works best for you is imperative</p>

        <p>The average male needs to eat around 2500 calories and the average female needs around 2000 calories. I know this may sound  like a nagging parent, but you should ensure<b>  you’re not just binging on snacks,</b> as you need <b>the right amount of carbs, fats (don’t be scared of them), and protein. </b>It helps to have a meal plan to  properly schedule and document your eating habits and ensure  you are  eating enough. </p>

        <p>If you  run into trouble making a meal plan, remember there are lots of meal plans online to assist you!  This website leads you to a great meal plan generator: <a href="https://www.eatthismuch.com/"> Eat This Much: The Automatic Meal Planner.</a> Obviously you<b> don’t need to follow the meal plans religiously </b>and you can definitely make some wiggle room for the foods you like. </p>

        <p>I am  also quite  into bodybuilding so I have a pretty strict meal plan, but I also have an insatiable sweet tooth. So instead of outright going “cold turkey” on sweets, I incorporate small amounts of it into my diet. This makes me feel good and helps me stick to my meals. Make sure your meal plan is enjoyable. Below is an actual demonstration of me enjoying my meals.</p>
        
        <div style="margin: auto; margin-bottom: 1rem; text-align: center;">
          <img src='../i/7/goku.gif' style='max-width: 70vw; max-height: 50vh;'>
        </div>
        
        
        <h4 style = "color: #00008B">Exercise</h4>
        
        <p>Obviously you should make sure you’re getting enough exercise. Exercise doesn’t need to be a whole CrossFit routine in the gym, although if that’s what you like go for it. <b>Exercise can be as simple as a 30-minute jog </b>throughout your neighbourhood a couple of days a week. You’ll come back <b>feeling refreshed and ready to tackle</b> whatever you have to do.</p> 

        <p>Make sure if you are exercising that you eat properly. The calorie guidelines I suggested are just basic guidelines. If you go to the gym a couple of times a week to lift weights, or maybe go on runs throughout your neighbourhood you need to eat more to fuel your body.</p>

        <h4 style = "color: #00008B">Sleep</h4>
        
        <p>Obviously make sure you’re <b>getting the right amount of rest too</b>. 7-9 hours of sleep is the average recommended amount, I know it can be hard getting that amount so at least <b>aim for 6 hours.</b> Your <b>circadian rhythm resets every 1hr and 30 minutes.</b> Most scientists <b>recommend that your circadian rhythm should repeat 5 times (7hr and 30 mins)</b> for a good night’s sleep, but <b>other people say that only repeating 4 times (6 hrs) is good enough.</b></p>

        

        <h3>Mental Health</h3>
        <h4 style = "color: #00008B">Personal Time</h4>
        
        <p>Next is mental health. This one isn’t as easy as physical health. Improving your physical health is pretty similar for everybody but improving mental health is unique to every person. The<b> key thing is to make sure you make space for some “you time”.</b> Find something you really like doing and dedicate at least thirty minutes a day  doing it. </p>

      <p>  I enjoy  the guitar so I dedicate quite  bit of time playing or writing songs. The idea is to make sure you have a <b>bit of time every single day to forget about your stresses</b> and allow your brain to relax. Besides that, there are a couple of other things you can do to improve your mental health. <b>Exercising and eating well can have a very positive effect on your mental health</b> (as can the occasional sweet treat), not to mention <b>sleeping enough is essential</b> to good brain health. </p>

        <p>You can see why I grouped physical and mental health together. There is a lot of <b>overlap between the two</b>. They aren’t as different as you may think. Improving one tends to improve the other. 
        </p>
        
        
        <h3>Social Health</h3>
        <h4 style = "color: #00008B">Don’t be a Lone Wolf</h4>
        <p><b>Social health is incredibly important. Having a good supportive group of friends and adults is essential.</b></p>
        
        <p>I know we all want to be like that badass Lone Wolf character that shows up in TV shows and movies but whether we like it or not <b>we need people</b>. There are lots of things we can do by ourselves but there’s an equal (if not, greater) amount of things we can’t do by ourselves.</p> 
        
     <h4 style = "color: #00008B">Be Mindful of Who You Surround Yourself With</h4>
        
        <p>The <b>people we surround ourselves with have a great impact on who we are and who we become.</b> A common proverb says that you can tell a lot about a <b>person based on their closest group of friends.</b> Although this may not be 100% true it definitely holds some truth. Make sure <b>you surround yourself with people who are good for you, and whom you feel comfortable about opening up with your problems. </b></p>

       <p> Likewise, <b>ensure you’re also there for your friends if they need you,</b> although it may not seem like it, <b>we’re all going through similar problems.</b> I think the best way to end this segment would be with a quote from one of my favorite TV characters, Uncle Iroh from Avatar the Last Airbender: <b>“while it is always best to believe in one’s self. A little help from others can be a great blessing”.</b></p>
        
        <div style="margin: auto; margin-bottom: 1rem; text-align: center;">
          <img src='../i/7/wiseMan.png' style='max-width: 70vw; max-height: 50vh;'>
        </div>




       <h3>Seeking Help</h3>
        <h4 style = "color: #00008B">Seeking Help is Perfectly Fine</h4>
       <p> A point that I cannot emphasize enough is to <b>seek help when you need it.</b> Sometimes we may let our health decline and we may need help. I want to stress to you that <b>seeking help is perfectly fine</b> and you’ll feel better as a result. </p>
<p>
        If you have a <b>physical health issue it’s important to see a doctor</b> but if you have a <b>mental health issue ensure  you have appropriate  support.</b> Support can be friends, parents, adults, or a therapist. <b>Canada has a lot of mental health resources you can find <a href = "https://www.canada.ca/en/health-canada.html">here.</a> </b>
</p>
        
        <h4 style = "color: #00008B">People Care About You</h4>

        <p>
        Personally, when I was in a bad place I didn’t want to bother anyone with my problems so I neglected telling people. <b>This caused a rift between my friends, family and I.</b> I did suffer a lot because of it but when I decided to <b>open up to my parents and friends they were incredibly understanding. </b></p>
<p>
        <b>People who actually care about you will not judge you for your issues,</b> rather they’ll do what they can to help you. When I finally <b>talked to a therapist she was very supportive and helped me out a lot. Most campuses have mental health counsellors</b> but there are <b>a lot of other resources you can reach out to.</b> We all find ourselves in a bad place at some point in our journey, make sure you’re mindful about it and <b>seek help when you need it.</b></p>


        
        <h3>Perseverance is Key</h3>
        <h4 style = "color: #00008B">The Car Dilemma</h4>
        
       <p> My final point I would like to get across is the <b>importance of perseverance.</b> Declining health is not your fault, I repeat, you are <b>NOT at fault for how you are feeling.</b> If your car is overused without proper maintenance, it’s bound to break down at one point. No matter how much you curse at it, it’s not the car’s fault. </p>


        <h4 style = "color: #00008B">Gaining Momentum</h4>
<p>
        That being said it’s your responsibility to repair your car, likewise,<b> it’s your responsibility to overcome your problems</b>. This is where perseverance comes in. You may have days like I am where all your motivation is gone and everything feels wrong. It may feel like the world is sprinting ahead while you’re being left behind. That’s when you need to <b>force yourself to start something</b>, just start it. </p>

        <p>The Law of Inertia states that an object at rest wants to stay at rest, likewise,<b> you need to give yourself that PUSH to keep yourself going.</b> Oftentimes just <b>starting something gives us the momentum we need to push ahead.</b> For example, doing a whole workout may seem daunting and you might lack the motivation to do it, but force yourself to start the warm-up. Just the warm-up can give the momentum you need to tackle the workout.</p> 
        
        
    <h4 style = "color: #00008B">Response To Problems</h4>

        
<p>        Remember that <b>no matter how bad things seem it’s hardly ever permanent, but how you respond to these problems will shape who you are in the future.</b> Perseverance is key! 
</p>
<p>        I’ll end this segment with another quote that really touched me. This quote is from Guy-Sensei from the show Naruto: <b>“Don’t let anything steer you off the path you’ve drawn for yourself. Forge ahead to the end! Stick to it! Be everything you can be!”</b>
</p>        
          
