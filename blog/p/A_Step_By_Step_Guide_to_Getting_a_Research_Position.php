        <div class="col-md-25" style="padding: 1em;" data-aos="fade-left" data-aos-offset="-200">
          
          <p>
            In my senior year of high school, I decided that I wanted to join an academic research project to learn more about the healthcare sector. 
            Unfortunately, I had no research experience and my only knowledge about healthcare came from my yearly doctor appointments. I remember spending 
            hours scouring the internet for a guide but only finding terribly written email templates and extremely obvious advice. Having gone through the 
            process numerous times over the past few years, I hope to provide a step-by-step guide to how someone with little to no experience can get a research 
            position. 
          </p>

          <h3>Step 1. Choose your field</h3>
          <p>
            First, you must decide the field of research that you want to go into. A common misconception that I had in high school was that research is 
            only conducted by mixing chemicals in a lab. In fact, research is being conducted across all fields of study from economics to engineering. 
            Most post-secondary institutions have a web page dedicated to showcasing their research. If you are having a hard time choosing a field of research, 
            that is probably the best place to start. 
          </p>

          <h3>Step 2. Create a contact list</h3>
          <p>Next, you have to compile a list of researchers to contact. Unlike applying for a job, you will be emailing researchers to join their team rather 
            than applying through a company’s website. A list of all researchers in a department is typically found on the website of most post-secondary 
            institutions. From there, you can easily Google their names to learn more about their backgrounds and research. It is extremely important that 
            you stay organized at this step, so I recommend that you make an excel spreadsheet with the following columns: </p>
          <ul>
            <li><b>Researcher Name</b> – make sure to include both their first and last name.</li>
            <li><b>Email Address</b></li>
            <li><b>Field of Research</b> – be as specific as possible. For example, put “gene therapy” rather than “biology.”</li>
            <li><b>Interesting Publication</b> – write down the name of a publication you found interesting. This will be important 
              later when we are writing an email to the researcher. </li>
            <li><b>Date contacted</b> – this is important so you know who you contacted and who you have yet to contact. </li>
          </ul>
          <p>Here is an example of what your excel spreadsheet should look like:</p>
          <div style="margin: auto; margin-bottom: 1rem; text-align: center;">
            <img src="../i/contacttable.PNG" style="width: 80vw; max-width: 631px; margin-left: auto; margin-right:auto;" alt="Example of Contact List"></img>
          </div>
          <p>
            I’m sure many of you are wondering “how many researchers should I be contacting?” While there is no set number for how many emails you should send out,
            you are obviously more likely to get a good offer if you send out more. If you have close to no experience in the field, I recommend sending out 75
            emails. I know… that’s a lot. But think about it from the researcher’s point of view. If they take you on their project, they will have to train you for
            a few months and then you probably won’t contribute anything significant to their project anyways. When you are applying for research positions, you are
            essentially betting that someone is nice enough to help train the next generation of students. The odds are not in your favour. 
          </p>
          <p>
            For my first position, I contacted over 100 researchers. My numbers looked something like this: 
          </p>
          <div class="row" style="display: flex; justify-content: center; align-items: center;">
            <div class="col-lg-3 mb-4">
              <ul>
                <li><b>100</b> contacted</li>
                <li><b>50</b> responses</li>
                <li><b>15</b> interviews</li>
                <li><b>5</b> offers</li>
                <li><b>1</b> accepted</li>
              </ul>
            </div>
            <div class="col-lg-13 mb-4">
              <img src="../i/ChrisSankey.png" alt="My Application Results" style="width: 30vw; min-width: 300px;"></img>
            </div>
          </div>
          <p>
            Just like getting your first job, your first research position will always be the hardest to find. As you get more experience in your field of study, 
            your response and offer rates will significantly increase. 
          </p>
          <h3>Step 3. Write the emails</h3>

          <p>At this step, you should have a list of 75+ researchers in an excel spreadsheet that you can contact. Now it is time for you to write up an email 
            template. This is basically a framework of what you will send to each researcher without the personalized parts filled in. The content should be 
            structured something like this: 
          </p>
          <ul>
            <li><b>Paragraph 1: Introduction</b> – introduce yourself by explaining your qualifications and why you want to do research in this field. </li>
            <li><b>Paragraph 2: Interest</b> – explain why you are interested in this researcher’s work specifically. This is where you can write about the researcher’s
            publication that you found interesting.</li>
            <li><b>Paragraph 3: Closing</b> – Thank the researcher for their time and tell them that you are more than willing to set up a meeting to learn more about their
            research.</li>
            <li><b>Attachments: Resume/CV</b> (required) and <b>transcript</b> (optional but recommended)</li>
          </ul>
          <p>Before we continue, I would like to provide a few words of advice: </p>
          <ul>
            <li><b>Don’t make the email too long</b> – researchers are super busy, and they will be less likely to read a 2000 word essay than a brief &lt 500-word email.</li>
            <li><b>Personalize… but not too much</b> – Pretty much every single guide on the internet will tell you to write a completely personalized email to each researcher.
            Is that necessary? No! That would take ages! Realistically, the only section that you should be personalizing is the part where you are talking about why you
            are interested in the researcher’s work. It takes a lot of time but trust me it will pay off. I know that many of you are going to skip this part in favour of
            a copy-paste template so here’s some motivation. If you were a researcher, would you be more likely to respond to some random student who sent you a
            copy-paste message or someone who seems genuinely interested in your life’s work? That’s right. If you are going to spend 5 hours making an excel spreadsheet
            with the contact information for 75 researchers, why wouldn’t you just spend another few hours making a good email that will impress them?</li>
            <li><b>You’re helping, NOT getting helped</b> – Frame this email as how you can help the researcher rather than how the researcher can help you. Remember – the odds
            are already not in your favour if you are asking a researcher for help so it will only increase your chances if you tell them how you can further their
            research objectives instead. </li>
          </ul>
          <p>Here is an example of our template in action:</p>
          <div style="width: 90%; margin: auto; margin-top: 1rem; margin-bottom: 1rem; border: 1px black solid; padding: 1rem">
            <em>
            <p>Dear Dr. <b>(insert last name)</b>,</p>
            <p>My name is Christopher Ng and I am a grade 12 student attending Sir Winston Churchill High School. I am writing to inquire about whether you have any
            opportunities for me to join your research team this summer. I will be entering into the University of British Columbia next year with the intention of
            studying pharmacology, which is why I would like to further develop my qualitative and quantitative research skills in the next few months. As a student with
            a 95% average and significant experience in various leadership programs in my school, I believe that I will be a valuable addition to your research team.</p>
            <p>I have recently taken interest in your research on <b>(insert subject)</b>. I was especially fascinated by the findings of your <b>(insert year)</b> publication which
            discussed <b>(insert topic of research paper)</b>. I was intrigued by <b>(insert interesting finding)</b>.</p>
            <p>I would appreciate the opportunity to discuss your research and how I can contribute to your team in the upcoming year. My CV and transcript are attached
            to give you a better understanding of my skills and leadership experience.</p>
            <p>Thank you for your time,</p>
            <p>Christopher Ng</p>
            </em>
          </div>
          <p>All you have to do now is fill in your template with the relevant information for each researcher and send it out.</p>
          <h3>Step 4: Interviews</h3>
          <p style="margin-bottom: 0px;">Within a few days, you should receive some emails back from researchers who are willing to set up an in-person meeting or call to learn more about you.
          Interviews with researchers are typically much more casual compared to a job interview. Most of them are really nice people and it usually feels like a coffee
          chat rather than a super serious interrogation. Researchers usually know how much space they have in their team before they respond to you, so speaking to a
          researcher will typically lead to an offer!</p>
          <br>
          <hr style="width: 30%; border-top: 3px double;">
          <br>
          <p>You are going to receive a lot of rejections. I received over 70 rejections the first time I applied. But guess what, that’s why we sent 75+ emails out. Just
          like getting your first job, getting your first research position is all about perseverance. If you play your cards right, you will only have to spend 12 hours
          finding contact information and drafting up emails to join a project. That’s like 1 day of work if you are really enthusiastic, which is a pretty good return on
          investment if you ask me. Good luck on your search and if you have any questions please feel free to email me at <a href="mailto:christopher.ng@inclineedu.org">christopher.ng@inclineedu.org</a>.  </p>

