          <h1 data-aos="fade-up" style="font-size:calc(2rem + 1vw);">A Step-By-Step Guide to Getting a Research Position </h1>
          <h2 data-aos="fade-up" style="color: white; font-size: calc(1.15rem + 1.15vw);">Learn how you can join a research project with little to no experience</h2>
